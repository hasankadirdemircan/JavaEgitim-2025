package for_ex;

public class LoopFor01 {
    public static void main(String[] args) {
        //int a =veritabanından siparişleri getir emaile göre;
        for(int i=10; i>0; i--) {
            System.out.println(i);
        }

        for(int i=0; i<10; i++) {
            System.out.println(i);
        }
    }
}
