public class ArithmeticOperators02 {

    public static void main(String[] args) {
      int number1 = 10;
      int number2 = 11;
      number1 = number1 + 1;
      System.out.println("number1: " + number1);
      number1++;
      System.out.println("number1: " + number1);


      number2 = number2 - 1;
      System.out.println("number2: " + number2);

      number2--;
      System.out.println("number2: " + number2);

    }
}
